function Read-Grid($path){
  $tmp=Join-Path $env:TEMP ("xl_"+[guid]::NewGuid().ToString("N"))
  [System.IO.Compression.ZipFile]::ExtractToDirectory($path,$tmp)
  [xml]$ss=Get-Content (Join-Path $tmp "xl\sharedStrings.xml") -Encoding UTF8
  $strings=New-Object System.Collections.ArrayList
  foreach($si in $ss.sst.si){
    $txt=""
    if($si.r){ foreach($run in $si.r){ $txt += [string]$run.t.'#text' + [string]$run.t } }
    elseif($si.t -is [string]){ $txt=$si.t }
    elseif($si.t){ $txt=[string]$si.t.'#text' }
    [void]$strings.Add($txt)
  }
  [xml]$sheet=Get-Content (Join-Path $tmp "xl\worksheets\sheet1.xml") -Encoding UTF8
  $g=@{}
  foreach($row in $sheet.worksheet.sheetData.row){
    foreach($c in $row.c){
      $val=$c.v
      if($c.t -eq 's'){ $val=$strings[[int]$c.v] }
      if($val -ne $null -and "$val" -ne ""){ $g[$c.r]=[string]$val }
    }
  }
  return $g
}
function IdxCol($n){ $s=""; while($n -gt 0){ $m=($n-1)%26; $s=[char](65+$m)+$s; $n=[int][math]::Floor(($n-1)/26) }; return $s }
function Norm($s){ $s -replace '１','1' -replace '２','2' -replace '３','3' -replace '４','4' -replace '５','5' -replace '０','0' }

function Build-Shogakubu($grid,$areaRow,$detailStart){
  $themes=@{}
  for($si=0; $si -lt 3; $si++){
    $r=$si+2; $stepNum=$si+1
    foreach($col in @("A","B","C")){
      $stageNum=([int][char]$col[0]-64)
      $key="$col$r"
      if($grid.ContainsKey($key)){
        $t=$grid[$key] -replace '^STEP[0-9０-９]+＿',''
        $themes["${stageNum}_${stepNum}"]=$t
      }
    }
  }
  $stages=[ordered]@{}
  for($ci=1; $ci -le 40; $ci++){
    $col=IdxCol $ci
    $hkey="$col$areaRow"
    if(-not $grid.ContainsKey($hkey)){ continue }
    $code=$grid[$hkey]; $parts=$code -split '_'
    if($parts.Count -lt 3){ continue }
    $area=$parts[0]
    $stageNum=[int](Norm(($parts[1] -replace 'L','')))
    $stepNum=[int](Norm(($parts[2] -replace 'S','')))
    $details=New-Object System.Collections.ArrayList
    for($r=$detailStart; $r -le $detailStart+30; $r++){
      $key="$col$r"
      if($grid.ContainsKey($key)){
        $v=$grid[$key].Trim()
        if($v.Length -gt 1 -and $v -ne "]]"){ [void]$details.Add($v) }
      }
    }
    $theme=$themes["${stageNum}_${stepNum}"]; if(-not $theme){ $theme="STEP$stepNum" }
    $stageName="小学部${stageNum}段階"
    if(-not $stages.Contains($stageName)){ $stages[$stageName]=New-Object System.Collections.ArrayList }
    [void]$stages[$stageName].Add([ordered]@{
      step=$stepNum; stepTheme=$theme; area=$area;
      label=("STEP"+$stepNum+"　"+$theme+"｜"+$area); details=$details
    })
  }
  return $stages
}

# 中学部グループ生成ヘルパー（領域 → 指導事項）
function G($area,$details){
  $d=New-Object System.Collections.ArrayList
  foreach($x in $details){ [void]$d.Add($x) }
  return [ordered]@{ step=$null; stepTheme=$area; area=$area; label=$area; details=$d }
}
function AddStage($stages,$name,$groups){
  $arr=New-Object System.Collections.ArrayList
  foreach($g in $groups){ [void]$arr.Add($g) }
  $stages[$name]=$arr
}

